# saleperson rita drives 2052 miles in 6 days.stopping at 2 towns each day.How many kilometer does she average between stops

totalMilesToDrive=2052
totalDays=6
townToStopPerDay=2
kmPerMile=1.60934
avgMiles=(totalMilesToDrive/totalDays)/townToStopPerDay;
# count miles to km
avgKM=avgMiles*kmPerMile;
print("She drive average of ",avgKM,"km Between every stop")